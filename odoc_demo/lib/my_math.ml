(**
 * [sum2 a b] computes the sum of a and b.
 * @param a the first number
 * @param b the second number
 * @return their sum
 *)
let sum2 a b = a + b

(**
 * [product2 a b] computes the product of a and b.
 * @param a the first number
 * @param b the second number
 * @return their product
 *)
let product2 a b = a * b